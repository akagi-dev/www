export function getPortalFromUrl(url: URL) {
  const parts = url.pathname.split('/').filter(Boolean);
  // After base path, first part should be portal (www or drift)
  const baseIndex = parts[0] === 'www' && parts[1] ? 1 : 0;
  const portal = parts[baseIndex];
  if (portal === 'www' || portal === 'drift') return portal;
  return 'www'; // default to www portal
}

export function getLangFromUrl(url: URL) {
  // Split pathname and filter out empty strings
  const parts = url.pathname.split('/').filter(Boolean);
  // After base path and portal, we get the language
  const baseIndex = parts[0] === 'www' && parts[1] ? 1 : 0;
  const portal = parts[baseIndex];
  const langIndex = (portal === 'www' || portal === 'drift') ? baseIndex + 1 : baseIndex;
  const lang = parts[langIndex];
  if (lang in languages) return lang as keyof typeof languages;
  return defaultLang;
}

export function useTranslations(lang: keyof typeof languages) {
  return function t(key: keyof typeof ui[typeof defaultLang]) {
    return ui[lang][key] || ui[defaultLang][key];
  }
}
